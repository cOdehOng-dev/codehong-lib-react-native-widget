export type HongColorKey = keyof typeof HongColor;

export function nameToHongColor(name: string | null | undefined): HongColorEntry | null {
  if (!name) return null;
  return Object.values(HongColor).find(c => c.colorName === name) ?? null;
}

export function hexToHongColor(hex: string | null | undefined): HongColorEntry {
  if (!hex) return HongColor.TRANSPARENT;
  return Object.values(HongColor).find(c => c.hex === hex) ?? HongColor.TRANSPARENT;
}

/**
 * Android ARGB hex(#AARRGGBB) → React Native 호환 hex(#RRGGBB 또는 #RRGGBBAA)로 변환
 */
export function hongColorHexToRNColor(hex: string | null | undefined): string {
  const h = hex ?? HongColor.TRANSPARENT.hex;
  const clean = h.startsWith('#') ? h.slice(1) : h;

  if (clean.length === 8) {
    const alpha = clean.slice(0, 2);
    const rgb = clean.slice(2);
    // 완전 불투명이면 #RRGGBB로 단순화
    if (alpha === 'FF' || alpha === 'ff') return `#${rgb}`;
    // React Native는 #RRGGBBAA 지원 (RN 0.63+)
    return `#${rgb}${alpha}`;
  }
  return `#${clean}`;
}
